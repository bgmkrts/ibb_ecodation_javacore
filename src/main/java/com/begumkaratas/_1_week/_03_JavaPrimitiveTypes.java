package com.begumkaratas._1_week;

public class _03_JavaPrimitiveTypes {

    public static void main(String[] args) {
        // Veri değişken isimlerini yazarken;

        // tam sayılar (4)
        byte b1=127;
        System.out.println("byte:"+b1);
        short s1=32767;
        System.out.println("short:"+s1);
        int i1=21474846;
        System.out.println("int:"+i1);
        long l1=924466656566646656L;
        System.out.println("Long:"+l1);

        // virgüllü (floating point)

        float f2=3.14f;
        System.out.println("float:"+f2);
        double d2=3.14;
        System.out.println("double:"+d2);

        boolean b3=true;
        System.out.println("boolean"+b3);

        char c4='a';
        System.out.println("char"+c4);

        //sabit

        final int MAX_STUDENT=100;
        System.out.println(MAX_STUDENT);

      /*  MAX_STUDENT=255;
        System.out.println(MAX_STUDENT);*/


    }
}
